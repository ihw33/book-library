def auto_tag(title: str, filepath: str) -> list[str]:
    """제목과 파일 경로를 기반으로 자동 태그 목록 반환.
    macOS HFS+ NFD 인코딩 이슈 처리를 위해 NFC로 정규화 후 비교.
    """
    # NFD → NFC 정규화 (macOS 파일시스템 대응)
    text = unicodedata.normalize("NFC", title + " " + filepath).lower()
    tags = set()
    for tag_name, keywords in TAG_RULES:
        for kw in keywords:
            if kw.lower() in text:
                tags.add(tag_name)
                break
    return sorted(tags)
